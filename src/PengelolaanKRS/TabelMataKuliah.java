    private void tampilandata(){
        mod = new DefaultTableModel();
        mod.addColumn("Id");
        mod.addColumn("Nama_Matkul");
        mod.addColumn("Kelas");
        mod.addColumn("Semester");
        tabelmatakuliah.setModel(mod);
        Connection conn=Koneksi.getConnection();
        try{
            java.sql.Statement smt = conn.createStatement();
            SQL = "select*from tb_matakuliah";
            java.sql.ResultSet res = smt.executeQuery(SQL);
            while(res.next()){
                mod.addRow(new Object[]{
                    res.getString("Id"),
                    res.getString("Nama_Matkul"),
                    res.getString("Kelas"),
                    res.getString("Semester"),
                });
                }
            }catch (SQLException e) {
                System.out.println(e);
        }
        
    }
    
    

