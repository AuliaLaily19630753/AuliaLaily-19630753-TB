    private void tabelmatakuliahMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelmatakuliahMouseClicked
        try{
             Connection conn = Koneksi.getConnection();
             int row = tabelmatakuliah.getSelectedRow();
             String tabel_klik = (tabelmatakuliah.getModel().getValueAt(row, 0).toString());
             java.sql.Statement smt = conn.createStatement();
             java.sql.ResultSet sql = smt.executeQuery("select * from tb_matakuliah where Id = '"+tabel_klik+"'");
              
             if (sql.next()){
                 String Id=sql.getString("Id");
                 textid.setText(Id);
                 String Nama_Matkul=sql.getString("Nama_Matkul");
                 textnama_makul.setText(Nama_Matkul);
                 String Kelas=sql.getString("Kelas");
                 textkelas.setText(Kelas);
                 String Semester=sql.getString("Semester");
                 textsemester.setText(Semester);
                 
             }
             
             }catch (SQLException e){}
    }//GEN-LAST:event_tabelmatakuliahMouseClicked

